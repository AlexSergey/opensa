/**
 * Asset registration for generated LOD models: allocate a free object id per model, emit an IDE `objs` section,
 * and splice an `IDE` line into `gta.dat` (IDEs load before IPLs). Shared by the LOD tools.
 */

/** Search window for free object ids: above the low special-model ranges, **at or below the stock model ceiling
 *  of 18630** — ids above it silently fail to load on stock SA (no adjuster), which is the "HD swapped, no LOD"
 *  symptom. The window must stay ≤ 18630; do not widen it. */
const ID_MIN = 4000;
const ID_MAX = 18630;
/** Default object flags for LOD billboards — the value the Proper-Fixes LOD-vegetation mod uses for tree LODs.
 *  (0x200084: alpha + draw-last + LOD-friendly.) */
const DEFAULT_FLAGS = 2097284;

/** IMG entry names are ≤ 23 bytes incl `.dff`; the model name is the entry base, so it must be ≤ 19 chars. */
const MAX_MODEL = 19;

/**
 * Assign each model a free object id — the lowest unused ids in the stock space (**not** necessarily contiguous;
 * ids needn't be consecutive, and a heavily-modded game rarely has a wide contiguous gap left below 18630).
 * Deterministic (models sorted, ids ascending). Staying ≤ 18630 means they load without an adjuster.
 */
export function allocateLodIds(models: readonly string[], used: ReadonlySet<number>): Map<string, number> {
  const sorted = [...new Set(models.map((m) => m.toLowerCase()))].sort();
  const free = freeIds(used, sorted.length);
  const ids = new Map<string, number>();
  sorted.forEach((model, i) => ids.set(model, free[i]));

  return ids;
}

/** An IDE `objs` section text — one row per model `id, model, txd, drawDistance, flags`, ordered by id. */
export function buildLodIde(
  ids: ReadonlyMap<string, number>,
  txd: string,
  drawDistance: number,
  flags: number = DEFAULT_FLAGS,
): string {
  const rows = [...ids]
    .sort((a, b) => a[1] - b[1])
    .map(([model, id]) => `${id}, ${model}, ${txd}, ${drawDistance}, ${flags}`);

  return [`# Generated by @opensa/map-placement — LOD models (${txd})`, 'objs', ...rows, 'end', ''].join('\r\n');
}

/**
 * The model name SA registers for a generated LOD (IDE `objs` + the `<model>.dff` IMG entry). Usually the LOD's
 * own name; when that would overflow the IMG entry limit, a short synthetic `<prefix><index>` alias (the DFF still
 * references its real texture inside the shared TXD, so visuals are unaffected).
 */
export function lodAlias(name: string, index: number, prefix = 'lodt'): string {
  const lower = name.toLowerCase();

  return lower.length <= MAX_MODEL ? lower : `${prefix}${index}`;
}

/** Splice `IDE <path>` into gta.dat right after the last IDE line (so it loads before the IPLs). */
export function patchGtaDat(dat: string, idePath: string): string {
  const eol = dat.includes('\r\n') ? '\r\n' : '\n';
  const lines = dat.split(/\r?\n/);
  let lastIde = -1;
  lines.forEach((line, i) => {
    if (/^\s*IDE\b/i.test(line)) {
      lastIde = i;
    }
  });
  const insert = `IDE ${idePath}`;
  if (lastIde < 0) {
    lines.unshift(insert);
  } else {
    lines.splice(lastIde + 1, 0, insert);
  }

  return lines.join(eol);
}

/** The lowest `count` unused ids within `[ID_MIN, ID_MAX]` (ascending; gaps allowed). */
function freeIds(used: ReadonlySet<number>, count: number): number[] {
  const ids: number[] = [];
  for (let id = ID_MIN; id <= ID_MAX && ids.length < count; id += 1) {
    if (!used.has(id)) {
      ids.push(id);
    }
  }
  if (ids.length < count) {
    throw new Error(`only ${ids.length} free object ids in [${ID_MIN}, ${ID_MAX}], need ${count}`);
  }

  return ids;
}
