/**
 * Stock GTA-SA HD tree/foliage model names — the set processed when the CLI is run **without `--in`** (impostors
 * are baked from the game's own `gta3.img`, no model/texture swap). SA has no data flag for "this is a tree"
 * (the generic `lod<name>` convention covers buildings too), so this is a curated list.
 *
 * Derived (then hand-curated): the model names of a large vegetation mod ∩ the stock game IDEs, minus the
 * `procobj.dat` scatter species (those go to `lod-procobj-generator`) and minus non-foliage the mod bundles
 * (rocks/columns, grass/flower/fern ground-cover, rubble, pots/planters, and already-`lod*` models). Models not
 * present in the target `gta3.img` are skipped (logged), so extra names are harmless.
 *
 * REVIEW: still a curated list — trim/extend per the target game's actual roster (e.g. desert cacti/joshua are
 * kept as billboard-able plants; drop any that look wrong as impostors).
 */

/**
 * Name-pattern classifier for models a vegetation folder bundles that are **not** billboard-able foliage — rocks /
 * columns, ground grass / flowers / fern / ivy / proc-patches, rubble debris, pots / planters, and already-`lod*`
 * models. Skips them when baking from a `--in` folder (the same "type" cut used to curate {@link SA_TREE_MODELS});
 * `procobj.dat` species are filtered separately.
 */
export function isNonTreeModel(model: string): boolean {
  return /rock|column|rubble|_rub_|^kb_|^pot_|planter|grasspatch|tallgrass|pflower|fern|ivy|procf|procg|fpatch|weegrs|procwee|^lod/.test(
    model.toLowerCase(),
  );
}

